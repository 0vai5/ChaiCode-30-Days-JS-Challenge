export const Difference = (num1, num2) => {
    return num1 - num2;
}

export const Division = (num1, num2) => {
    return num1 / num2;
}

export const Multiplication = (num1, num2) => {
    return num1 * num2;
}


const modulas = (num1, num2) => {
    return num1 % num2;
}

export default modulas



